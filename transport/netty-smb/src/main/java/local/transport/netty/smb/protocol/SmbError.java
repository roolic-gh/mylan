    SmbError(int code) {
        this.code = code;
    }

    public int code() {
        return code;
    }

    public static SmbError fromCode(int code) {
        for (var err : values()) {
            if (err.code == code) {
                return err;
            }
        }
        return STATUS_OTHER;
    }
